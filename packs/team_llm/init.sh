#!/bin/bash
set -euo pipefail
# Puget Systems — Team LLM Initialization
# Detects GPUs, recommends a model, writes .env, launches the stack.

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

echo -e "${BLUE}============================================================${NC}"
echo -e "${BLUE}   Puget Systems — Team LLM Setup (vLLM)${NC}"
echo -e "${BLUE}============================================================${NC}"

# --- Source shared libraries ---
INIT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )"
source "$INIT_DIR/scripts/lib/gpu_detect.sh"
source "$INIT_DIR/scripts/lib/vllm_model_select.sh"
source "$INIT_DIR/scripts/lib/vllm_monitor.sh"
source "$INIT_DIR/scripts/lib/env_write.sh"

# --- GPU Detection ---
echo ""
echo -e "${YELLOW}[1/3] Detecting GPUs...${NC}"

if ! detect_gpus; then
    echo -e "${RED}✗ nvidia-smi not found. NVIDIA drivers required.${NC}"
    exit 1
fi

echo -e "${GREEN}✓ Found ${GPU_COUNT}x ${GPU_NAME} (${VRAM_GB} GB each, ${TOTAL_VRAM} GB total)${NC}"
if [ "$IS_BLACKWELL" = true ]; then
    echo -e "${GREEN}  Blackwell GPU detected (compute ${COMPUTE_CAP}) → using CUDA 13.0 images${NC}"
fi

# --- Cache Proxy Status ---
if [ -f .env ]; then
    # Safe key extraction — only read known keys, never execute .env as code
    CACHE_PROXY=$(grep '^CACHE_PROXY=' .env 2>/dev/null | tail -1 | cut -d= -f2- || true)
fi

if [ -n "${CACHE_PROXY:-}" ]; then
    echo -e "${GREEN}✓ Cache Proxy: $CACHE_PROXY${NC}"
else
    echo -e "${YELLOW}⚠ No cache proxy configured (downloads go direct).${NC}"
    echo "  To enable, add CACHE_PROXY=http://<ip>:3128 to .env"
fi

# --- Model Selection (uses shared library) ---
echo ""
echo -e "${YELLOW}[2/3] Select a model${NC}"
echo ""
echo "  Available models (based on ${TOTAL_VRAM} GB total VRAM):"
echo ""
show_vllm_model_menu
echo ""
read -p "Select [1-${MENU_MAX}]: " CHOICE

if ! select_vllm_model "$CHOICE"; then
    if [ -z "$VLLM_MODEL_ID" ]; then
        echo "Exiting."; exit 0
    fi
    # VRAM insufficient — exit
    exit 1
fi

if [ -z "$VLLM_MODEL_ID" ]; then
    echo -e "${RED}No model selected.${NC}"
    exit 1
fi

# --- Write Config ---
echo ""
echo -e "${YELLOW}[3/3] Writing configuration...${NC}"

# Write or update .env
# Write a clean .env using the structured env_write helpers
write_env_header "team_llm" ".env"
write_env_comment "Generated by init.sh on $(date)"
write_env_blank ".env"
write_env_var "MODEL_ID" "$VLLM_MODEL_ID" ".env"
write_env_var "VLLM_IMAGE" "$VLLM_IMAGE" ".env"
write_env_var "GPU_COUNT" "$VLLM_GPU_COUNT" ".env"
write_env_var "MAX_CONTEXT" "$VLLM_MAX_CTX" ".env"
write_env_var "GPU_MEMORY_UTILIZATION" "$VLLM_GPU_MEM_UTIL" ".env"
write_env_var "REASONING_ARGS" "$VLLM_REASONING_ARGS" ".env"
write_env_var "THINKING_ARGS" "$VLLM_THINKING_ARGS" ".env"
write_env_var "TOOL_CALL_ARGS" "$VLLM_TOOL_CALL_ARGS" ".env"
write_env_var "EXTRA_VLLM_ARGS" "$VLLM_EXTRA_ARGS" ".env"
write_env_var "DTYPE" "$VLLM_DTYPE" ".env"
# Preserve cache proxy from previous config if present
if [ -n "${CACHE_PROXY:-}" ]; then
    write_env_var "CACHE_PROXY" "$CACHE_PROXY" ".env"
fi

echo -e "${GREEN}✓ Configuration written to .env${NC}"
echo ""
echo "  Model:    $VLLM_MODEL_ID"
echo "  GPUs:     $VLLM_GPU_COUNT"
echo "  Context:  ${VLLM_MAX_CTX:-auto (vLLM sizes from VRAM)}"
echo "  Memory:   $VLLM_GPU_MEM_UTIL utilization"
echo ""

read -p "Start the stack now? (Y/n): " START
if [[ "$START" != "n" && "$START" != "N" ]]; then
    echo -e "${BLUE}Starting vLLM server...${NC}"

    # Validate .env before launch — catch corruption early
    if ! validate_env ".env"; then
        echo -e "${RED}Fix .env issues before launching.${NC}"
        exit 1
    fi

    COMPOSE_EXIT=0
    docker compose up -d || COMPOSE_EXIT=$?
    echo ""
    
    if [ $COMPOSE_EXIT -ne 0 ]; then
        echo -e "${RED}✗ Failed to start containers. Check logs:${NC}"
        echo -e "  ${BLUE}docker compose logs${NC}"
    else
        LOCAL_IP=$(hostname -I | awk '{print $1}')
        echo -e "${GREEN}✓ Stack starting.${NC}"
        echo -e "  Chat UI:  ${BLUE}http://localhost:3000${NC}"
        echo -e "  API:      ${BLUE}http://localhost:8000/v1${NC}"
        echo -e "  Network:  ${BLUE}http://${LOCAL_IP}:3000${NC}"
        echo ""
        
        echo -e "${YELLOW}Waiting for model to download and load...${NC}"
        echo "  (This may take 5-30 minutes depending on model size and bandwidth)"
        echo ""
        wait_for_vllm "puget_vllm" "$VLLM_MODEL_SIZE_GB"
    fi
else
    echo "Run 'docker compose up -d' when ready."
fi
